type GetAreaLogic struct {
	logx.Logger
	ctx    context.Context
	svcCtx *svc.ServiceContext
}

func NewGetAreaLogic(ctx context.Context, svcCtx *svc.ServiceContext) *GetAreaLogic {
	return &GetAreaLogic{
		Logger: logx.WithContext(ctx),
		ctx:    ctx,
		svcCtx: svcCtx,
	}
}

var areaBuf []byte

func init() {
	areaBuf, _ = json.Marshal(httpresult.Success(resp))
}

func (l *GetAreaLogic) GetArea(w http.ResponseWriter, r *http.Request) {
	_, err := w.Write(areaBuf)
	if err != nil {
		logx.WithContext(l.ctx).Errorf("get area error:%+v", err)
		return
	}
	//logx.WithContext(l.ctx).Infof("get area buf length :%d", n)
	return
}

